<h3>8. TDS or Tax deducted at source</h3>
<p>Let us understand the concept of TDS with an example. As tax is deducted based on your income tax slab, similarly, if you earn interest from a Fixed Deposit, the bank also deducts TDS. Since the bank does not know your tax slabs, they usually deduct TDS @ 10%, unless you have not mentioned your PAN (in that case a 20% TDS may be deducted). TDS is deducted by the person making payment whether it is salary payment, payment for contract, professional fee, etc. The TDS must be deducted at certain percentage on an amount payable based on the rules prescribed by the income tax department. For example, an employer will estimate the total annual income of an employee and deduct tax on his Income if his Taxable Income exceeds INR 5,00,000. The employee can also provide form 15g/15h in case he proves that his taxable income will be less than Rs 5,00,000 after claiming all the deductions.</p>
<br>
<h3>9. Calculating Tax Payable</h3>
<p>Now as we have discussed our income sources and deductions that you can claim to reduce your taxable income, let us understand how to calculate the tax payable on your taxable income. There are 4 tax slabs for individuals under old scheme and 7 tax slabs under new scheme, tax slabs and tax rates according to tax slabs are applied and final tax payable is calculated. On the tax computed additional cess @ 4% is charged and then final tax payable is computed. It is important to note that from this tax payable, the TDS amount that has already been deducted shall be reduced to arrive at the amount of tax payable from your pocket.</p>
<br>
<h3>10. Standard Deduction</h3>
<p>As per the Budget 2018, salaried employees were entitled to a standard deduction of Rs 40,000 from the gross salary. This standard deduction replaced the medical reimbursement amounting to INR 15,000 and transport allowance amounting to Rs. 19,200 in a financial year.
Now as per Budget 2019, the standard deduction allowed to the salaried employees has been raised from 40,000 to 50,000. It is available to all the salaried employees.</p>
 
 
