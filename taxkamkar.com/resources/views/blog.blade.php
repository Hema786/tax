			<h3>7. Make Section 80C your best friend</h3>
<p>Section 80C is the highest tax saving section which can reduce your gross total income by Rs 1,50,000. Given below are some of the most popular investment options under this section.</p>
<h3>a. PPF</h3>
<p>One of the most widely used deductions under 80C is deposits to Public Provident Fund or PPF. When you open a PPF account, you need to deposit a minimum of INR 500 and a maximum of INR 1,50,000 in a year. Money deposited earns interest @ 7.1% p.a.(currently) compounded annually. Interest earned is exempt and withdrawal of deposited amount at the end of lock-in period is also exempt. However, we suggest PPF deposits to people aged between 40-50 years as there is 15 years lock-in period under this scheme.</p>
<h3>b. Tax-saving FD</h3>
<p>The taxpayers who are most conservative can invest their hard-earned money in fixed deposits. Fixed Deposits assure capital protection as well as a considerable interest income for investors. To get tax benefits under 80C, there is a lock-in period for at least 5 years. It is safe, but unlike PPF the interest income from it is taxable.</p>
<h3>c. Tax-saving mutual funds or ELSS</h3>
<p>This is one of our most preferred instruments as the return on investment is highest among all the tax saving ways available under section 80C and lock-in period is only 3 years which is the lowest among all the options available for claiming deduction. Equity Linked Savings Scheme is becoming popular among people for its historically higher performancs. However, we do not advise ELSS to those who are highly risk averse because ELSS involves market risk.</p>
 
